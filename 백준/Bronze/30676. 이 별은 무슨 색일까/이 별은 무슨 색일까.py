p = int(input())
if 380 <= p < 425 :
    print('Violet')
elif 425 <= p < 450 :
    print('Indigo')
elif 450 <= p < 495 :
    print('Blue')
elif 495 <= p < 570 :
    print('Green')
elif 570 <= p < 590 :
    print('Yellow')
elif 590 <= p < 620 :
    print('Orange')
else:
    print('Red')
